# Python math Module
import math
# constants
print(math.e)
print(math.pi)
# methods
print(math.fabs(-5))

square_root = math.sqrt(25)
print(square_root)

print(math.pow(2, 5))
print(math.pow(3, 5))

print(math.factorial(99))

# up rounding
print(math.ceil(4.2))

# floor rounding 
print(math.floor(4.7))

#sin,cos,ctg,tg
print(math.sin(math.radians(90)))
print(math.cos(math.radians(30)))
print(math.tan(math.radians(90)))